#include <bits/stdc++.h>
#include <vector>
#define ll long long

using namespace std;

vector<string> g;
vector<vector<pair<int, int>>> adj;
vector<vector<bool>> v;

int main() {
  ll n, q;
  cin >> n >> q;
  g.resize(n);
  int root = -1;
  for (ll i = 0; i < n; i++) {
    cin >> g[i];
    if (root == -1) {
      for (int j = 0; j < n; j++) {
        if (g[i][j] == '#') {
          root = i * n + j;
        }
      }
    }
  }

  ll r1, c1, r2, c2;
  for (ll i = 0; i < q; i++) {
    cin >> r1 >> c1 >> r2 >> c2;
    r1--;
    r2--;
    c1--;
    c2--;
  }
}

