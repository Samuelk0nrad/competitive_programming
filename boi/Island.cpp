#include <bits/stdc++.h>
#define ll long long

using namespace std;

vector<string> g;
vector<pair<ll, ll>> con;

int main() {
  ll n, q;
  cin >> n >> q;
  g.resize(n);
  con.resize(n + 5, {-1, -1});
  string s;
  for (ll i = 0; i < n; i++) {
    cin >> s;
    g[i] = s;
    if (i >= 1) {
      bool ss = false;
      bool sl = false;
      for (ll j = 0; j < n; j++) {
        if (s[j] == '#') {
          ss = true;
        } else {
          ss = false;
        }
        if (g[i - 1][j] == '#') {
          sl = true;
        } else {
          sl = false;
        }
        if (ss == true && sl == true && con[i].first == -1) {
          con[i].first = j;
        } else if ((ss == false || sl == false) && con[i].first != -1 &&
                   con[i].second == -1) {
          con[i].second = j - 1;
        }
      }
      if (con[i].first != -1 && con[i].second == -1) {
        con[i].second = n - 1;
      }
    }
  }
  ll r1, c1, r2, c2;
  for (ll i = 0; i < q; i++) {
    cin >> r1 >> c1 >> r2 >> c2;
    r1--;
    r2--;
    c1--;
    c2--;
    if (r1 < r2) {
      ll tmp = r1;
      r1 = r2;
      r2 = tmp;
      tmp = c1;
      c1 = c2;
      c2 = tmp;
    }
    pair<ll, ll> cur = {r1, c1};
    ll mov = 0;
    while (cur.first != r2) {
      if (cur.second >= con[cur.first].first &&
          cur.second <= con[cur.first].second) {

      } else if (cur.second < con[cur.first].first) {
        mov += abs(con[cur.first].first - cur.second);
        cur.second = con[cur.first].first;
      } else if (cur.second > con[cur.first].second) {
        mov += abs(cur.second - con[cur.first].second);
        cur.second = con[cur.first].second;
      }
      mov++;
      cur.first--;
    }
    mov += abs(cur.second - c2);
    cout << mov << "\n";
  }
}

